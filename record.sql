  select
    `dr`.`sid` AS `sid`,
    `mb`.`name` AS `name`,
    `ft`.`food_type` AS `food_type`,
    `dr`.`quantity` AS `quantity`,
    `dr`.`diet_time` AS `diet_time`
  from
    (
      (
        `record_diet_record` `dr`
        join `member` `mb`
      )
      join `record_food_type` `ft`
    )
  where
    (
      (`dr`.`member_sid` = `mb`.`sid`)
      and (`dr`.`food_sid` = `ft`.`sid`)
    )
);

